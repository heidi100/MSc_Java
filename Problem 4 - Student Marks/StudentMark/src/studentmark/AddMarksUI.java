    /**
     * The action performed event for the add button on the add marks UI.
     * This will add the users input of mark to the correct module for the student.
     * @param evt 
     */
    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        
        // Sets the variables to get the data from the text field and combo box.
        int m = Integer.parseInt(markField.getText()); 
        String mb = moduleBox.getSelectedItem().toString();
        
        int cIndex = courses.indexOf(course);
        int mIndex = courses.get(cIndex).getModule().indexOf(module);
        int stuIndex = courses.get(cIndex).getStudents().indexOf(student);
        
        // Sets the student number for the course to increase by one student and adds the marks.
        int studNum = courses.get(cIndex).getModule().get(mIndex).getStudentNumber() +1;
        courses.get(cIndex).getModule().get(mIndex).setStudentNumber(studNum);
        courses.get(cIndex).getStudents().get(stuIndex).addModMark(mb, m);
        
        // Displays a message dialog stating that the mark has been added.
        JOptionPane.showMessageDialog(this, "The Mark has been added.");
        // Returns to the main UI.
        StudentMainUI stMain = new StudentMainUI(courses);
        stMain.setVisible(true);
        this.setVisible(false);
               
    }//GEN-LAST:event_addButtonActionPerformed

