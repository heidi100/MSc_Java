    /**
     * Initalises the combo box.
     */
    public void initComboBox()
    {
        // Loops through the courses.
        for(Course c : courses)
        {
            // Checks if the course code is equal to the students course.
            if(c.getCode().equals(student.getCourse()))
            {
                course = c;
                
                // Loops through modules.
                for(Module mod : c.getModule())
                {
                    // Checks if the student does not have marks for the module already.
                    if(!student.getMarks().containsKey(mod.getCode()))
                    {
                        // Adds the module code to the model.
                        module = mod;
                        modulesModel.addElement(mod.getCode());
                    }
                }
            }
        }
        // Sets the module box to dsplay the model.
        moduleBox.setModel(modulesModel);
    }

